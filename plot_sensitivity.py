from io import StringIO
df = pd.read_csv(StringIO(csv_text))

# ── plot config ───────────────────────────────────────────────────────────────
params = {
    "num_sa":        "Number of Systolic Arrays (num_sa)",
    "sa_dim":        "Systolic Array Dimension (sa_dim)",
    "num_vu":        "Number of Vector Units (num_vu)",
    "hbm_bw_GBps":  "HBM Bandwidth (GB/s)",
    "vmem_size_MB":  "SRAM Scratchpad Size (MB)",
}

os.makedirs("plots", exist_ok=True)

for param, xlabel in params.items():
    sub = df[df["param"] == param].copy()

    # ── TTFT plot ──────────────────────────────────────────────────────────────
    fig, ax = plt.subplots(figsize=(6, 4))
    ax.plot(sub["value"], sub["TTFT_sec"], marker="o", color="steelblue", linewidth=2)
    ax.set_xlabel(xlabel, fontsize=12)
    ax.set_ylabel("TTFT (seconds)", fontsize=12)
    ax.set_title(f"Prefill Latency (TTFT) vs. {param}", fontsize=13)
    ax.grid(True, linestyle="--", alpha=0.5)
    fig.tight_layout()
    fig.savefig(f"plots/TTFT_{param}.png", dpi=150)
    plt.close(fig)

    # ── TPOT plot ──────────────────────────────────────────────────────────────
    fig, ax = plt.subplots(figsize=(6, 4))
    ax.plot(sub["value"], sub["TPOT_ms"], marker="s", color="tomato", linewidth=2)
    ax.set_xlabel(xlabel, fontsize=12)
    ax.set_ylabel("TPOT (ms / token)", fontsize=12)
    ax.set_title(f"Decode Latency (TPOT) vs. {param}", fontsize=13)
    ax.grid(True, linestyle="--", alpha=0.5)
    fig.tight_layout()
    fig.savefig(f"plots/TPOT_{param}.png", dpi=150)
    plt.close(fig)

print("Done! 10 plots saved to ./plots/")
